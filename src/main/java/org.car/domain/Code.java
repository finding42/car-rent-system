package org.car.domain;

public class Code {
    private long codeID;
    private String codeName;

    public long getCodeID() {
        return codeID;
    }

    public void setCodeID(long codeID) {
        this.codeID = codeID;
    }

    public String getCodeName() {
        return codeName;
    }

    public void setCodeName(String codeName) {
        this.codeName = codeName;
    }
}
